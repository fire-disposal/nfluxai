def get_all_diseases() -> list:
    """获取所有疾病名称列表"""
    all_diseases = []
    for category, diseases in DISEASE_PATTERNS.items():
        all_diseases.extend(diseases)
    return all_diseases


def get_all_diagnoses() -> list:
    """获取所有护理诊断列表"""
    all_diagnoses = []
    for category, diagnoses in NURSING_DIAGNOSES.items():
        all_diagnoses.extend(diagnoses)
    return all_diagnoses


def find_diseases_in_text(text: str) -> list:
    """在文本中查找疾病名称"""
    found = []
    all_diseases = get_all_diseases()
    
    for disease in all_diseases:
        if disease in text:
            found.append(disease)
    
    return list(set(found))


def find_diagnoses_in_text(text: str) -> list:
    """在文本中查找护理诊断"""
    found = []
    all_diagnoses = get_all_diagnoses()
    
    # 先匹配完整诊断名称
    for diagnosis in all_diagnoses:
        if diagnosis in text:
            found.append(diagnosis)
    
    # 匹配简短形式（如"焦虑"、"疼痛"等）
    short_patterns = [
        ("焦虑", "焦虑"),
        ("恐惧", "恐惧"),
        ("疼痛", "疼痛"),
        ("营养失调", "营养失调：低于机体需要量"),
        ("体液不足", "体液不足"),
        ("体液过多", "体液过多"),
        ("清理呼吸道无效", "清理呼吸道无效"),
        ("气体交换受损", "气体交换受损"),
        ("活动无耐力", "活动无耐力"),
        ("知识缺乏", "知识缺乏"),
        ("有感染的危险", "有感染的危险"),
        ("有受伤的危险", "有受伤的危险"),
        ("皮肤完整性受损", "皮肤完整性受损"),
        ("睡眠型态紊乱", "睡眠型态紊乱"),
    ]
    
    for pattern, full_name in short_patterns:
        if pattern in text and full_name not in found:
            found.append(full_name)
    
    return list(set(found))


def find_symptoms_in_text(text: str) -> list:
    """在文本中查找症状"""
    found = []
    for symptom in SYMPTOMS:
        if symptom in text:
            found.append(symptom)
    return list(set(found))


def classify_content_type(title: str, content: str) -> str:
    """根据标题和内容分类内容类型"""
    text = title + " " + content[:500]
    
    for content_type, markers in NURSING_PROCESS_MARKERS.items():
        for marker in markers:
            if marker in title or marker in text:
                return content_type
    
    # 检查疾病信息
    disease_markers = ["病因", "发病机制", "临床表现", "诊断要点", "治疗要点", "预后"]
    for marker in disease_markers:
        if f"【{marker}" in title or f"【{marker}】" in title:
            return "disease_info"
    
    return "general"


